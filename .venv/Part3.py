import pandas as pd

# Создание данных
data = {
    'Имя': ['Алексей', 'Борис', 'Виктория', 'Галина', 'Дмитрий', 'Елена', 'Жанна', 'Иван', 'Ксения', 'Леонид'],
    'Математика': [90, 85, 78, 92, 88, 76, 95, 89, 84, 91],
    'Физика': [85, 87, 90, 78, 86, 82, 88, 80, 85, 89],
    'Химия': [78, 80, 85, 90, 88, 92, 87, 84, 81, 83],
    'Биология': [92, 88, 90, 85, 87, 89, 94, 86, 88, 90],
    'Литература': [88, 84, 87, 89, 90, 85, 86, 88, 87, 90]
}

# Создание DataFrame
df = pd.DataFrame(data)

# Шаг 2: Вывод первых строк DataFrame
print("Первые строки DataFrame:")
print(df.head())

# Шаг 3: Вычисление средней оценки по каждому предмету
average_scores = df.mean(numeric_only=True)
print("\nСредние оценки по каждому предмету:")
print(average_scores)

# Шаг 4: Вычисление медианной оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианные оценки по каждому предмету:")
print(median_scores)

# Шаг 5: Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"\nПервый квартиль (Q1) для математики: {Q1_math}")
print(f"Третий квартиль (Q3) для математики: {Q3_math}")
print(f"Межквартильный размах (IQR) для математики: {IQR_math}")

# Шаг 6: Вычисление стандартного отклонения
std_deviation = df.std(numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:")
print(std_deviation)
